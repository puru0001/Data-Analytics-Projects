-- creating DB Named Inventory---
create database Inventory;
--using db Inventory--
use Inventory

--creating table supplier--
create table supplier (
		SID varchar(6) primary key,
		SNAME varchar(30) not null,
		SADD varchar(30) not null,
		SCITY varchar(10) check(SCity='delhi'),
		SPHONE bigint unique,
		EMAIL varchar(20) );

--creating table product--
create table product (
		PID VARCHAR(5) PRIMARY KEY,
		PDESC VARCHAR(20) NOT NULL,
		PRICE INT CHECK(PRICE>0),
		CATEGORY VARCHAR(5) CHECK(CATEGORY ='IT' OR CATEGORY ='HA' OR CATEGORY = 'HC'),
		SID VARCHAR(6) REFERENCES SUPPLIER(SID) );

--creating table STOCK--
CREATE TABLE STOCK(
		PID VARCHAR(5) REFERENCES PRODUCT(PID),
		SQTY INT CHECK(SQTY >= 0),
		ROL INT CHECK(ROL > 0),
		MOQ INT CHECK(MOQ >= 5)	);

--creating table CUST--
CREATE TABLE CUST(
		CID VARCHAR(5) PRIMARY KEY,
		CNAME VARCHAR(30) NOT NULL,
		ADDR VARCHAR(25) NOT NULL,
		CITY VARCHAR(20) NOT NULL,
		PHONE BIGINT NOT NULL,
		EMAIL VARCHAR(20) NOT NULL,
		DOB DATE CHECK(DOB < '01-01-2000')
		);

--creating table ORDERS--
CREATE TABLE ORDERS(
		OID VARCHAR(5) PRIMARY KEY,
		ODATE DATE,
		PID VARCHAR(5) REFERENCES PRODUCT(PID),
		CID VARCHAR(5) REFERENCES CUST(CID),
		OQTY INT CHECK(OQTY >= 1)	);



